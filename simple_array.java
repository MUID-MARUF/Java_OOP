public class simple_array {

    public static void main(String args[]){

    int arr1[] = {1,2,3};
    for(int i = 0; i<3; i++){
        System.out.println(arr1[i]);
    }

    System.out.println();

    String[] computers = {"HP", "Dell", "Apple"};

    for(int j = 0; j<3; j++){
        System.out.println(computers[j]);
    }

    System.out.println();
    
    double[] battery_life = {2.3, 4.6, 15.3};

    for(int f = 0; f<3; f++){
        System.out.println(battery_life[f]);
    }
    }
}
